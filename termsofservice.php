		clearInterval(animateLevelUpIn);
		clearTimeout(animateLevelUpTi1);
		clearTimeout(animateLevelUpTi2);
		clearTimeout(animateLevelUpTi3);
		clearTimeout(animateLevelUpTi4);
		}

